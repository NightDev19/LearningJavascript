/*
    ++ Increment
    -- Decrement
    += Increment by value
    -= Decrement by value
    *= Multiply by value
    /= Divide by value 
    %= Modulus by value
    **= Exponentiate by value 
*/

let point = 10;
point++; //point = point + 1;
console.log(point);

let score = 20;
score--; // score = score - 1;
score--; // score = score - 1;
console.log(score); // 18

score += 10; // score = score + 10;
console.log(score);

score -= 5; // score = score - 5;
console.log(score);

let age = 21;
age *= 2; // age
console.log(age);

age /= 2;
console.log(age);

age %= 6;
console.log(age);

age **= 2;
console.log(age);
